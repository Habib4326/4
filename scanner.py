if __name__ == "__main__":
    extract_m3u_from_livetv()
    extract_all_texas_channels()
    extract_from_new_livetv()
    
    final_filename = "all_channels.m3u"
    with open(final_filename, 'w', encoding='utf-8') as f:
        f.write("\n".join(all_m3u_lines))
        
    print("\n" + "="*70)
    print(f"  🎉 SUCCESS! Consolidated M3U Playlist Saved To: {final_filename}")
    print("="*70)
